    public void abrirFormulario (JFrame f){
        if (f.isVisible()){
            JOptionPane.showMessageDialog(null, "Já existe uma instância do aplicativo aberta");
        }
        else {
            f.setVisible(true);
            
            dispose();

        }
    }

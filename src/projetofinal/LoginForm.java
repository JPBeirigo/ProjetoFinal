    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        //Evento gerado quando o usuário clicar com o o botão esquerdo do mouse ou
        //a barra de espaço quando o botão estiver selecionado
        
        String email =txtEmail.getText();
        String senha = new String(txtSenha.getPassword());
                
        if (email.equals("1") && senha.equals("1")) {
            JOptionPane.showMessageDialog(null, "Bem vindo ao Sistema");
            abrirFormulario(new PrincipalForm());
        } 
        else if (email.equals("2") && senha.equals("1")){
            JOptionPane.showMessageDialog(null, "Bem vindo ao Sistema");
            txtEmail.setText("");
            txtSenha.setText("");
            txtEmail.requestFocus();
            abrirFormulario(new PrincipalForm());
        }
        else {
            JOptionPane.showMessageDialog(null, "Usuário/Senha Inválida");
            
            //Limpar os campos do texto
            txtEmail.setText("");
            txtSenha.setText("");
            txtEmail.requestFocus();
        }
                
    }//GEN-LAST:event_btnEntrarActionPerformed

